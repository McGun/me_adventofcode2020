function examinePath (int $step, int $down, array $map) {
    $step_max_length = strlen($map[0]);
    $next_step = $step;
    $count_trees = 0;
    $count_lines = count($map);

    for ($i=0; $i < $count_lines; $i++) {
        if ($i < 1) {
            continue;
        }
        
        if ($down === 2 && $i % $down !== 0) {
            // $new_map[] = $map[$i].$map[$i].$map[$i];
            continue;
        }
        
        # Handle repeating pattern
        if ($next_step >= $step_max_length) {
            $next_step = ($next_step - $step_max_length);
        }

        # Check for tree or pass
        if (substr($map[$i], $next_step, 1) === '#') {
            $count_trees++;
        }

        $next_step = $next_step + $step;

    }
    return $count_trees;
}

$count_trees_on_all_paths = [];

foreach ($paths as $path) {
    $trees = examinePath($path->step, $path->down, $map);
    $count_trees_on_all_paths[] = $trees;
    printf(
        "Right %d and down %d\nCounted trees: %d\n",
        $path->step,
        $path->down,
        $trees
    );
}

printf(
    "Product: %d\n",
    array_product($count_trees_on_all_paths)
);
